package hello;

import java.util.Iterator;
import java.util.List;

import org.apache.commons.collections4.IteratorUtils;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.ModelAttribute;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.ResponseBody;

@Controller    // This means that this class is a Controller
@RequestMapping
public class MainController {
    @Autowired // This means to get the bean called userRepository
    // Which is auto-generated by Spring, we will use it to handle the data
    private UserRepository userRepository;

    @GetMapping(path="/add") // Map ONLY GET Requests
    public @ResponseBody String addNewUser (@RequestParam String name
            , @RequestParam String email) {
        // @ResponseBody means the returned String is the response, not a view name
        // @RequestParam means it is a parameter from the GET or POST request

        User n = new User();
        n.setName(name);
        n.setEmail(email);
        userRepository.save(n);
        return "Saved";
    }

    @PostMapping(value = "/update")
    public void addNewUserFromButtonQuery(@ModelAttribute("name") String name
        , @ModelAttribute("email") String email)
    {
        System.out.println(name + "\\");
        User user = new User();
        user.setName(name);
        user.setEmail(email);
        // @ResponseBody means the returned String is the response, not a view name
        // @RequestParam means it is a parameter from the GET or POST request
        userRepository.save(user);
    }

    @GetMapping(path = "/updateuser")
    public void getAllUsers(Model model)
    {
        // This returns a JSON or XML with the users
        Iterator<User> allUsers = userRepository.findAll().iterator();
        List<User> userList = IteratorUtils.toList(allUsers);
        model.addAttribute(userList);
    }

    @PostMapping(value="/fun")
    public String addNewUserFromButton (Model model, @ModelAttribute("name") String name
            , @ModelAttribute("email") String email) {
        // @ResponseBody means the returned String is the response, not a view name
        // @RequestParam means it is a parameter from the GET or POST request
        System.out.println(name+"\\");
        System.out.println(email+"\\");
        if (!name.equals("")&&!email.equals(""))
        {
            User n = new User();
            n.setName(name);
            n.setEmail(email);
            userRepository.save(n);
        }
        return getAllUsersHTML(model);
    }

    @GetMapping(path="/fun")
    public String getAllUsersHTML(Model model) {
        // This returns a JSON or XML with the users
        Iterator<User> allUsers=userRepository.findAll().iterator();
        List<User>  userList= IteratorUtils.toList(allUsers);
        model.addAttribute(userList);
        return "greeting";
    }



}